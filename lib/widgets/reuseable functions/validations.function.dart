String? nameValidator(String value) {
  if (value.isEmpty) {
    return "Please Enter Your Name";
  }
  if (!(value.startsWith(RegExp(r'[A-Z]')))) {
    return "Please,Start With Capital Letter";
  }
  return null;
}
